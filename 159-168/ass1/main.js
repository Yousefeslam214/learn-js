let date = new Date();
let birthDate =new Date(13-9-2004);
let dateDiff = date - birthDate;




console.log(`${dateDiff} mile Seconds`);  //mile secand
console.log(`${dateDiff/1000} Seconds`);  // secand
console.log(`${dateDiff/1000/60} Minutes`);  // Minutes
console.log(`${dateDiff/1000/60/60} Hours`);  // Hours
console.log(`${dateDiff/1000/60/60/24} Days`);  // Days
console.log(`${dateDiff/1000/60/60/24/12} Months`);  // Months
console.log(`${dateDiff/1000/60/60/24/12/365} Years`);  // Years

